// function for day 2 part 1 that takes a string and returns nothing
func day2Part1(input string) {
	// split input by new line
	splitInput := splitByNewLine(input)
	// loop through each line
	// total score
	totalScore := 0
	for _, line := range splitInput {
		// first character in line is player1, third is player2
		player1 := line[0]
		player2 := line[2]
		// convert player1 and player2 to rock paper scissors
		player1RPS := convertCharToRPS(player1)

		player2RPS := convertCharToRPS(player2)

		// if player2 is Y, set player2RPS to player1RPS
		if player2 == 'Y' {
			player2RPS = player1RPS
		}
		// if player2 is Z, player2RPS is winning move with player1RPS
		if player2 == 'Z' {
			player2RPS = winningMove(player1RPS)
		}
		// if player2 is X, player2RPS is losing move player1RPS
		if player2 == 'X' {
			player2RPS = losingMove(player1RPS)
		}

		// score player 2 in rock paper scissors.
		// if player 2 wins, 6 points, if draw 3 points, player 2 loses 0 points
		// score for player 2
		score := 0

		// if player1 is rock and player2 is paper, player2 wins
		if player1RPS == 'R' && player2RPS == 'P' {
			score = 6
		}
		// if player1 is rock and player2 is scissors, player1 wins
		if player1RPS == 'R' && player2RPS == 'S' {
			score = 0
		}
		// if player1 is paper and player2 is rock, player1 wins
		if player1RPS == 'P' && player2RPS == 'R' {
			score = 0
		}
		// if player1 is paper and player2 is scissors, player2 wins
		if player1RPS == 'P' && player2RPS == 'S' {
			score = 6
		}
		// if player1 is scissors and player2 is rock, player2 wins
		if player1RPS == 'S' && player2RPS == 'R' {
			score = 6
		}
		// if player1 is scissors and player2 is paper, player1 wins
		if player1RPS == 'S' && player2RPS == 'P' {
			score = 0
		}
		// if player1 and player2 are the same, it's a tie
		if player1RPS == player2RPS {
			score = 3
		}

		// if player 2 plays paper, 2 point bonus
		// if player 2 plays rock, 1 bonus point
		// if player 2 plays scissors, 3 point bonus
		if player2RPS == 'P' {
			score += 2
		}
		if player2RPS == 'R' {
			score += 1
		}
		if player2RPS == 'S' {
			score += 3
		}
		// print score
		//fmt.Println(score)
		// add to total score
		totalScore += score
	}
	// print score
	println(totalScore)
}

// new function to convert char to rock paper scissors char
func convertCharToRPS(char byte) byte {
	// X or A returns R, Y or B returns P, Z or C returns S
	if char == 'X' || char == 'A' {
		return 'R'
	}
	if char == 'Y' || char == 'B' {
		return 'P'

	}
	if char == 'Z' || char == 'C' {
		return 'S'
	}
	return ' '
}

// function to return winning RPS move based on partners move
func winningMove(char byte) byte {
	// if char is rock, return paper
	if char == 'R' {
		return 'P'
	}
	// if char is paper, return scissors
	if char == 'P' {
		return 'S'
	}
	// if char is scissors, return rock
	if char == 'S' {
		return 'R'
	}
	return ' '
}

// function to return losing RPS move based on partners move
func losingMove(char byte) byte {
	// if char is rock, return scissors
	if char == 'R' {
		return 'S'
	}
	// if char is paper, return rock
	if char == 'P' {
		return 'R'
	}
	// if char is scissors, return paper
	if char == 'S' {
		return 'P'
	}
	return ' '
}

// function to split string by new line
func splitByNewLine(input string) []string {
	return strings.Split(input, "\n")
}

